import numpy as np
a = np.array([10, 20, 30, 40, 50])
new_a = np.insert(a,2,100,axis=0)
print(new_a)
new_a = np.insert(a,2,[100,200],axis=0)
print(new_a)
a_2d = np.array([[1, 2, 3], [4, 5, 6]])
new_a_2d = np.insert(a_2d,1,[10,20,30],axis=0)
print(new_a_2d)
new_a_2d = np.insert(a_2d,2,[10,20],axis=1)
print(new_a_2d)
new_a_2d = np.insert(a_2d,2,100,axis=1)
print(new_a_2d)
new_a_2d = np.insert(a_2d,2,[100,200],axis=1)
print(new_a_2d)
